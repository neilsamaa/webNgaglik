      <p>
        Bila dirangkum NGAGLIK dari sisi <i>aksara</i> berarti <b>menggapai cita-cita</b> dengan berdasarkan <b>keutamaan atau kebenaran</b>.
        Dari dasar ini dicoba dibuat sebuah <b>citra Ngaglik</b> menjadi sebuah <b>kelurahan yang mengedepankan keutamaan dalam melakukan segala hal</b>.
      </p>
    
      <br>
      <h5>Pengamalan</h5>
      <p>
        Kita coba tempatkan kata Ngaglik dalam konteks turunan (jw.Leregan ) dari kata Ngegloning panggonan kan meglik-meglik, yang
        artinya Ngaglik harus menempatkan diri sebagai kelurahan yang terlihat dari jauh karena tempatnya yang di atas ( ini dalam
        kacamata orang luar Ngaglik ). Tetapi ke dalam masyarakat desa Ngaglik maka Ngaglik harus Ngeglo yang artinya harus transparan.
      </p>
      <p>
        Dari dua landasan pikir tersebut ( basic believe ) Ngaglik berbenah diri, Ngaglik membangun, Ngaglik harus mampu menjadi
        tauladan, Ngaglik harus terdepan, tetapi <b>semua harus transparan dan harus selalu bersih dalam artian bersih dari kejahatan,
        bersih dari kemungkaran, masyarakatnya guyub rukun tidak ada yang eker-ekeran karepe dewe hasil yang diharapkan.</b>
      </p>
      <p>
        Kalau semua sudah demikian maka rasa aman akan tertanam di hati masyarakat. Kalau sudah merasa aman tidak ada curiga mencurigai,
        maka dengan mudah warga dapat melakukan pekerjaan dengan guyub rukun dari <b>hati yang bersih</b>, disampaikan dengan fasih insya Allah
        hasilnya <b>akan jernih</b>.
      </p>

      <br>
      <h5>Kerangka Pengamalan</h5>
      <ul>
        <li>Dimana pelayan masyarakat harus memberi jalan terang pada warganya.</li>
        <li>Kelurahan Ngaglik harus tetap aman.</li>
        <li>Segala bentuk pangupa jiwa di Ngaglik dapat memberi kesejahteraan warga.</li>
        <li>Bila telah tercapai kesejahteraan,masyarakat harus bersyukur dalam bentuk :
          <ul>
            <li>Memberikan sedekah dengan makan bersama (dalam artian zakat dan tasyakur, bukan dalam artian korupsi bareng-bareng).</li>
            <li>Menggelar kesenian sebagai ajang pelepasan (ajang kebersamaan dalam suasana hati yang gembira, bukan datang dengan igit-igit).</li>
          </ul>
        </li>
        <li>Tetapi semua harus dilambari dengan selalu ingat pada Sang Khalik</li>
        <li>Masyarakat Ngaglik harus memahami ke wilayah :
          <ul>
            <li>Mulai dari tanah yang dihuni oleh manusia terdahulu.</li>
            <li>Tempat-tempat pemberi kesejahteraan ( air sumber, sungai atau tempat konservasi ).</li>
            <li>Dan tak lupa warga harus tahu tempat-tempat kritis di wilayahnya.</li>
          </ul>
        </li>
        <li>Dengan tujuan warga dapat melestarikan dan berhati-hati bila ada di tempat tersebut.</li>
        <li>Semua ini harus dipahami bersama dan dilakukan dengan ikhlas.</li>
      </ul>

      <br>
      <h5>Pengamalan tersebut digambarkan dalam sebuah prosesi kirap bersih desa.</h5>
      <ol>
        <li><b>Pambuka.</b> Sekelompok masyarakat membawa alat pembersih ( sapu lidi, sapu ijuk ) terutama para ibu, yang mempunyai dua makna :
          <ul>
            <li><b>Pertama</b>, bahwa sebelum ada desa pasti ada pendahulu yang membabat desa, yang pada umumnya disebut sebagai bedah desa.
              Karena secara pasti tidak diketahui, maka secara simbolis diambillah seorang perempuan. Karena perempuan pasti yang
              memperanakkan semua manusia di dunia.</li>
              <li><b>Kedua</b>, menyapu dan membersihkan jalan, berarti keberadaan unsur-unsur di belakang itu sudah disepakati dan disetujui oleh masyarakat.</li>
          </ul>
        </li>
        <li><b>Pamong</b>. Pamong dan pelayan kelurahan berjalan di depan dengan membawa suluh atau lampu penerangan.</li>
        <li><b>Tamtama</b>. Disusul oleh kelompok pemuda dan masyarakat sebagai kekuatan penjaga wilayah kelurahan, dengan membawa
          segala persiapan menghadapi bahaya ( toya, pentung, tombak, kebyok pemadam api, gantol pawuhan, genter pembersih dsb ).</li>
        <li><b>Upa Jiwa</b>. Rombongan produsen mulai pangan, sandang, papan, jasa perdagangan, jasa yang lain, pegawai swasta amaupun negeri.</li>
        <li><b>Nugraha</b>. Semua yang diperoleh manusia merupakan anugerah dari Sang Khalik untuk manusia, dan diwujudkan dalam
          arak-arakan tumpeng yang akan dipurak sebagai tumpeng babonan sebagai tanda kerukunan. Tumpeng di bagian bawah masih berbeda-beda,
          ada ikan asin, kulupan, perkedel, sambel goring, empal, goreng ayam dsb. Makin ke atas mengerucut menjadi satu titik.
          Itu semua merupakan symbol manusia, dimana ada tataran yang berbeda-beda dan bila diwujudkan dalam suatu kesatuan pandang yang
          digambarkan dengan kerucut <b>nasi putih</b> atau linambaran kautaman atau <b>nasi kuning</b> yang bermakna aku wus wening( jernih ).
          Kemudian dimakan bersama ( dipurak), maka yang asin, yang pedas, yang tawar, yang gurih akan menjadi sebuah rasa yang nikmat
          dan enak. Itu sebuah anugerah.</li>
        <li><b>Imbang-imbang</b>. Ditampilkan dalam sebuah kesenian asli dan kesenian yang ada di Kelurahan Ngaglik. Maknanya, manusia
          harus punya <b>pelepas kepenatan</b> yang mempunyai sifat-sifat <b>keindahan, manusiawi dan harmonisasi</b>. Disini ditampilkan dua makna.
          <b>Pertama</b>. Manusia jangan hanya bekerja dan bekerja tanpa punya sisi lain untuk menghibur diri. <b>Kedua</b>. Semua tetabuhan tak mungkin
          akan baik bila tanpa harmonisasi. Seni suara juga harus mengikuti notasi yang ada. Seni lukis harus mengetahui komposisi warna,
          bentuk dsb. Maknanya dalam bermasyarakat dan hidup berdampingan, manusia harus punya norma atau aturan. Hal tersebut untuk
          mengatasi perbedaan. Sehingga diperlukan <b>aturan yang baik dan pengatur yang baik</b> supaya semuanya terlihat baik.</li>
        <li><b>Tulodho</b>. Kelompok Alim ulama dan tokoh masyarakat sebagai lambang Tut wuri handayani dan tepo palupi. Bermakna sebagai
          contoh keberhasilan cita-cita dunia, yang terlihat dalam sebuah pilihan. Ada yang menjadi ulama dan menjadi tokoh masyrakat.
          Kelompok ini merupakan sisi lain dari penutup gendang dimana sisi lainnya adalah pamong sehingga kehadiran kelompok ini kalau
          mampu tut wuri handayani, maka irama roda kehidupan di Ngaglik akan baik dan pantas dijadikan desa teladan.</li>
      </ol>

      <br>
      <h5>Lintasan Kirap</h5>
      <ul>
        <li>Mengenai rute prosesi adalah ke tempat makam leluhur ( untuk memperingati dan mendoakan agar diberi tempat yang layak,
          hal ini untuk ucapan terima kasih atas pemilihan tempat di Ngaglik ).</li>
        <li>Tempat pemberi sumber kehidupan ( sumber air, sungai atau hutan konservasi ) biasanya dalam bentuk pohon besar, disini
          tujuannya untuk melestarikan dan menjaga tempat tersebut untuk keseimbangan alam.</li>
        <li>Tempat kritis ( misalkan ujung jurang, jalan berkelok yang berbahaya, tebing yang mudah longsor ) untuk mengingatkan pada
          warga bahwa daerah itu perlu diperbaiki agar tidak menjadi tempat kritis.</li>
      </ul>

