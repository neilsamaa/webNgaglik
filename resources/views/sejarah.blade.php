      <h5>Asal Usul Kelurahan Ngaglik</h5>
      <p>Titik awal mula (Starting Time) dari sejarah Kota Batu adalah ketika Batu memperoleh statusnya sebagai kota pada tanggal 17
        Oktober 2001.Jadi, pada waktu sekarang (tahun 2011), durasi waktu kota Batu baru mencapai sepuluh tahun. Berarti pula sejarah
        kota Batu hanya berisi ragam peristiwa yang terjadi di Batu dalam kurun waktu Sembilan tahun.
      </p>
      <p>
        Hal ini berbeda dengan sejarah daerah Batu yang meliputi durasi waktu yang amat panjang,yakni sejak diperoleh bukti tertua
        tentang kehidupan manusia purba di Batu pada masa bercocok tanam dalam jaman prasejarah hingga peristiwa penting teraktual,
        yakni perolehan status Batu sebagai kota. Begitu juga kelurahan Ngaglik, dimana sejak jaman dahulu merupakan bagian dari
        sejarah kota Batu.
      </p>
      <p>
        Meskipun tidak banyak bukti yang menunjukkan kapan berdirinya desa/kelurahan Ngaglik namun dari bukti sejarah yang ditemukan
        dapat kita paparkan bahwa awalnya desa/kelurahan Ngaglik adalah salah satu desa yang terpencil dan posisinya adalah yang paling
        atas dibanding dengan desa/kelurahan yang lain dan dihuni oleh seorang wanita yang bernama Mbah Sayu Mentaram dengan suaminya.
      </p>
      <p>
        Dasar dinamakan desa/kelurahan Ngaglik adalah pada waktu Mbah sayu Mentaram membuka hutan (babat alas) di daerah Batu kuno untuk
        tempat tinggal dan bercocok tanam. Disanalah beliau menghuni sebuah gubug yang dibangun pada posisi yang paling atas yang disebut
        <b>RUMAH ANGGLIK-ANGGLIK</b>.
      </p>
      <p>
        Ada juga yang berpendapat bahwa ada batu yang berdiri di atas batu lain yang apabila posisi batu yang atas disentuh ataupun
        digoyang tidak akan jatuh hanya <b>OGLAK-AGLIK</b> atau hanya bergoyang dan tidak sampai roboh.
      </p>

      <br>
      <h5>Filosofi</h5>
      Dari unsur aksara NGAGLIK.
      <table class="table">
